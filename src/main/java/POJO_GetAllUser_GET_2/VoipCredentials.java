package POJO_GetAllUser_GET_2;

public class VoipCredentials {
	private String provider;
	private String sub_provider;
	private String username;
	private String password;
	private String domain;
	private String agent_key;
	private String extention;

	public String getProvider() {
		return provider;
	}

	public void setProvider(String provider) {
		this.provider = provider;
	}

	public String getSub_provider() {
		return sub_provider;
	}

	public void setSub_provider(String sub_provider) {
		this.sub_provider = sub_provider;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getAgent_key() {
		return agent_key;
	}

	public void setAgent_key(String agent_key) {
		this.agent_key = agent_key;
	}

	public String getExtention() {
		return extention;
	}

	public void setExtention(String extention) {
		this.extention = extention;
	}
}
