package POJO_Constant_GET;

public class ConferenceCall {
	  public boolean sales;
	    public boolean pre_sales;
	    public boolean post_sales;
		public boolean isSales() {
			return sales;
		}
		public void setSales(boolean sales) {
			this.sales = sales;
		}
		public boolean isPre_sales() {
			return pre_sales;
		}
		public void setPre_sales(boolean pre_sales) {
			this.pre_sales = pre_sales;
		}
		public boolean isPost_sales() {
			return post_sales;
		}
		public void setPost_sales(boolean post_sales) {
			this.post_sales = post_sales;
		}
}
