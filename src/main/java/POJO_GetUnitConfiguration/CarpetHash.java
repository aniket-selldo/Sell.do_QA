package POJO_GetUnitConfiguration;

public class CarpetHash {
	    private double sq_ft;
	    private double sq_yd;
	    private double sq_meter;
	    private double acres;
		public double getSq_ft() {
			return sq_ft;
		}
		public void setSq_ft(double sq_ft) {
			this.sq_ft = sq_ft;
		}
		public double getSq_yd() {
			return sq_yd;
		}
		public void setSq_yd(double sq_yd) {
			this.sq_yd = sq_yd;
		}
		public double getSq_meter() {
			return sq_meter;
		}
		public void setSq_meter(double sq_meter) {
			this.sq_meter = sq_meter;
		}
		public double getAcres() {
			return acres;
		}
		public void setAcres(double acres) {
			this.acres = acres;
		}
}
